def _write(path: Path, content: str, *, force: bool = False) -> bool:
    if path.exists() and not force:
        print(f"  [SKIP] {path} (exists, use --force to overwrite)")
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    print(f"  [CREATED] {path}")
    return True


def main(argv: Optional[list[str]] = None) -> int:
    parser = argparse.ArgumentParser(description="Bootstrap MNEMOS integration in a consumer app")
    parser.add_argument("--target", required=True, help="Path to consumer application root")
    parser.add_argument("--workspace", default="mnemos_integration", help="Output folder name (default: mnemos_integration)")
    parser.add_argument("--force", action="store_true", help="Overwrite existing generated files")
    args = parser.parse_args(argv)

    target = Path(args.target).resolve()
    if not target.exists():
        print(f"[ERROR] Target path does not exist: {target}")
        return 1

    workspace = target / args.workspace
    workspace.mkdir(parents=True, exist_ok=True)

    print(f"\n== MNEMOS Onboarding ==")
    print(f"Target: {target}")
    print(f"Workspace: {workspace}\n")

    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    _write(workspace / "mnemos_boundary.py", _BOUNDARY_TEMPLATE, force=args.force)
    _write(workspace / ".env.mnemos", _ENV_TEMPLATE, force=args.force)
    _write(workspace / "mnemos_smoke_spec.json", _SMOKE_TEST_TEMPLATE, force=args.force)
    _write(workspace / "MNEMOS_QUICKSTART.md",
           _QUICKSTART_TEMPLATE.format(timestamp=timestamp), force=args.force)

    # Context manifest for AI dev tools
    manifest = {
        "generator": "mnemos_onboard.py",
        "generated_at": timestamp,
        "target": str(target),
        "workspace": str(workspace),
        "artifacts": {
            "boundary": "mnemos_boundary.py",
            "env_template": ".env.mnemos",
            "smoke_spec": "mnemos_smoke_spec.json",
            "quickstart": "MNEMOS_QUICKSTART.md",
        },
    }
    _write(workspace / "context_manifest.json",
           json.dumps(manifest, indent=2), force=args.force)

    print(f"\n✅ Onboarding complete. See {workspace / 'MNEMOS_QUICKSTART.md'} to get started.\n")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
