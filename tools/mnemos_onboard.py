_BOUNDARY_TEMPLATE = '''\
"""
MNEMOS Boundary Adapter — auto-generated by mnemos_onboard.py
================================================================

Pre-wired boundary for calling the MNEMOS memory service.
Customize the methods below for your application's use case.
"""

from mnemos_sdk import MnemosClient, MnemosConfig


class MnemosBoundary:
    """Application boundary for the MNEMOS memory service."""

    def __init__(self, prefix: str = "MNEMOS"):
        self._config = MnemosConfig.from_env(prefix)
        self._client = MnemosClient(self._config)

    def ensure_ready(self) -> bool:
        """Wait for MNEMOS to become available. Call at app startup."""
        return self._client.wait_until_ready()

    @property
    def healthy(self) -> bool:
        resp = self._client.health()
        return resp.ok

    def store(self, content: str, *, source: str = "", tags: list[str] | None = None,
              metadata: dict | None = None):
        """Store a single document as an engram."""
        doc = {
            "content": content,
            "source": source,
            "neuro_tags": tags or [],
            "metadata": metadata or {},
        }
        resp = self._client.index([doc])
        if not resp.ok:
            raise RuntimeError(f"MNEMOS index failed: {resp.error}")
        return resp.data.get("result", {})

    def recall(self, query: str, *, top_k: int = 5):
        """Search MNEMOS and return ranked hits."""
        hits = self._client.search(query, top_k=top_k)
        return [
            {"content": h.engram.get("content", ""), "score": h.score, "tier": h.tier}
            for h in hits
        ]

    def forget(self, engram_id: str):
        """Delete an engram from all tiers."""
        resp = self._client.delete_engram(engram_id)
        if not resp.ok:
            raise RuntimeError(f"MNEMOS delete failed: {resp.error}")
        return resp.data.get("result", {})
'''

